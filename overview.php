        <h2>General description</h2>
        <p>In this module, you will get introduced into methods and techniques you can use for you own project. Though DAT minor is neither a programming nor an electrical engineering course, some basic knowledge of both is required in order to get the most out of this minor: when you are familiar with these basics, you will know what is possible and what not â€“ and how to get help or more information.</p>

        <p>The module consists of five pressure cooker lab courses on five (hopefully consecutive) Fridays of three hours each (times differ: check DAT Calendar), one week of working and one planairy demonstration. </p>

        <p>During the courses, general theory and practical applications will be introduced in a planairy format. After this introduction, you will work for ten to thirty minutes on several hands-on exercises, so you can get to grips with these materials (both physical as intellectual). During these minutes, teachers and TA's will walk around to answer (and ask) questions. When the exercises are finished, the course will continue planairy again with the next topic. This cycle is repeated until all the materials have been treated (or, more likely, the time is up).</p>

        <p>Every lab course has <em>preperations</em> and <em>assignments</em>, both of which are required: we assume you have done the preperation <i>before</i> the lab course and will check your elaborations of the assignments.</p>
    </section>

    <section id="assessment">
        <h1>Assessment</h1>
        <p>Following the four weeks period, you have two weeks to create a project for <a href="assessment.html">the assessment</a>. You need to work with a partner on this assessment. In week 7 of the minor, plenary presentations of all the elaborations will be held. This will actually be the first graded part of the minor and we go for beers afterwards in celebration of this fact.</p>

        <h2>Self assessment</h2>
        <p>Apart from the project, you are required to deliver a <em>self-assessment</em>: a form in which you state and reflect on your current position within the field of design, art and technology, how this module has helped you to determine this position, and the following steps that you wish to set in the remainder of the minor.</p>
    </section>


